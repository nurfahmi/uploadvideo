/**
 * Get translated string. Supports {placeholder} interpolation.
 * @param {string} key - Translation key like 'settings.title'
 * @param {object} [params] - Optional params for interpolation
 * @returns {string}
 */
export function t(key, params) {
  const lang = state.lang || getLanguage();
  const str = translations[lang]?.[key] ?? translations.en[key] ?? key;
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? '');
}

export function getLanguage() {
  // Default to Indonesian — primary audience; English stays available via
  // Settings toggle for anyone who explicitly switches.
  return localStorage.getItem(STORAGE_KEY) || 'id';
}

export function setLanguage(lang) {
  localStorage.setItem(STORAGE_KEY, lang);
  state.lang = lang;
  set('lang', lang);
}

export function initI18n() {
  const saved = getLanguage();
  state.lang = saved;
}
